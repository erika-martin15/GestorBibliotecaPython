class Libro:
    def __init__(self, codigo, titulo, autor, fecha_publicacion, num_paginas, genero, isbn, editorial, codigo_socio):
        self.codigo = codigo
        self.titulo = titulo
        self.autor = autor
        self.fecha_publicacion = fecha_publicacion
        self.num_paginas = num_paginas
        self.genero = genero
        self.isbn = isbn
        self.editorial = editorial
        self.codigo_socio = codigo_socio
